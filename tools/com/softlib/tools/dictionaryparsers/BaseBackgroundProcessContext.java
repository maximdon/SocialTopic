package com.softlib.tools.dictionaryparsers;

public abstract class BaseBackgroundProcessContext {

	protected int maxNumTickets;

	public BaseBackgroundProcessContext(int maxNumTickets) {
		this.maxNumTickets = maxNumTickets;
	}

	public BaseBackgroundProcessContext() {
		this(Integer.MAX_VALUE);
	}

	public int getMaxNumTickets() {
		return maxNumTickets;
	}
	
	public void setMaxNumTickets(int maxNumTickets)
	{
		this.maxNumTickets = maxNumTickets;
	}
}
